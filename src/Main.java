/**
 * HotelBookingApplication
 *
 * This class represents the entry point of the Hotel Booking System.
 * It demonstrates how a Java application begins execution using the
 * main() method and prints basic application information to the console.
 *
 * The program prints a welcome message along with the application
 * name and version, then terminates.
 *
 * @author Sachnoor
 * @version 1.0
 */
class HotelBookingApplication {

    /**
     * Main method – the entry point of the Java application.
     * The JVM starts execution from this method.
     *
     * @param args command-line arguments passed to the program
     */
    public static void main(String[] args) {

        // Welcome message
        System.out.println("Welcome to the Hotel Booking System");

        // Application name and version
        System.out.println("Application: BookMyStay");
        System.out.println("Version: 1.0");

        // Program termination message
        System.out.println("Application started successfully.");
    }
}